# Operasi Penjumlahan dengan number

# Masukan data untuk di hitung
bilangan1 = int(input("Masukkan bilangan bulat pertama: "))
bilangan2 = int(input("Masukkan bilangan bulat kedua: "))

# Rumus menghitung jumlah
jumlah = bilangan1 + bilangan2

# Menampilkan hasil
print(f"Jumlah dari {bilangan1} dan {bilangan2} adalah {jumlah}.")
